// Юр-тексты (оферта, политика) как данные: каждый абзац — набор «прогонов» (текст либо ссылка),
// чтобы рендер не знал разметки. Кириллица проходит через typo(). Ссылки на /offer, /privacy —
// внутренние (роутер), остальные (сайт, mailto) — внешние.

export interface LegalLink {
  text: string;
  href: string;
}

export type LegalRun = string | LegalLink;

export type LegalBlock =
  | { kind: "paragraph"; runs: readonly LegalRun[] }
  | { kind: "list"; items: readonly (readonly LegalRun[])[] };

export interface LegalSection {
  heading: string;
  blocks: readonly LegalBlock[];
}

export interface LegalDoc {
  title: string;
  /** Строка редакции над заголовком (со ссылкой на сайт). */
  intro: readonly LegalRun[];
  sections: readonly LegalSection[];
}

const MAILTO = `mailto:${SUPPORT_EMAIL}`;

function paragraph(...runs: readonly LegalRun[]): LegalBlock {
  return { kind: "paragraph", runs };
}

function list(...items: readonly (readonly LegalRun[])[]): LegalBlock {
  return { kind: "list", items };
}

function siteLink(): LegalLink {
  return { text: SITE_LABEL, href: SITE_URL };
}

function emailLink(): LegalLink {
  return { text: SUPPORT_EMAIL, href: MAILTO };
}

